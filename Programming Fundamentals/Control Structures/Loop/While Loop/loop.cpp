#include <iostream>

using namespace std;

int main(){

    int i = 0; // Declare an integer variable 'i' and initialize it to 0.

    // Start of the while loop. It will continue as long as 'i' is less than 5.
    while (i < 5) {
        cout << i << "\n"; // Print the current value of 'i' followed by a newline.
        i++; // Increment 'i' by 1 in each iteration.
    }

    return 0; // Return 0 to indicate successful program execution.
}
